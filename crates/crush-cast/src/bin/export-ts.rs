//! Export TypeScript declarations for the full CAST AST.
//!
//! Run with:
//!   cargo run -p crush-cast --bin export-ts --features ts-export
//!
//! Output is written to `crates/core/crush-cast/bindings/cast.d.ts`.

use crush_cast::ai::{
    AIExpression, AIMetadata, AIStatement, AdaptationAction, AdaptationStrategy, AdaptationType,
    BackoffStrategy, CollaborationPattern, DelegationStrategy, DiscoveryStrategy, ErrorHandling,
    ExecutionContext, ExecutionStrategy, KnowledgeType, LearningSource, LearningStrategy,
    LearningTarget, ParameterSchema, Priority, RetentionPolicy, ToolCall, ToolSchema,
};
use crush_cast::{
    CastType, DomMutationType, DomQueryType, Expression, ExternalResourceType, Function,
    ImportStatement, MatchArm, Pattern, Program, Statement,
};
use serde_json::Value;
use ts_rs::{Config, TS};

fn export_type<T: TS + 'static>() -> String {
    let cfg = Config::default();
    let raw = T::export_to_string(&cfg).unwrap();
    // Strip generated header and import lines, keeping only the type declaration.
    raw.lines()
        .skip_while(|line| {
            let trimmed = line.trim();
            trimmed.starts_with("//") || trimmed.starts_with("import")
        })
        .collect::<Vec<_>>()
        .join("\n")
        .trim()
        .to_string()
}

fn main() {
    // Collect declarations for every CAST type plus JsonValue.
    let declarations: Vec<String> = vec![
        export_type::<Value>(), // JsonValue
        export_type::<Program>(),
        export_type::<Function>(),
        export_type::<CastType>(),
        export_type::<Statement>(),
        export_type::<Expression>(),
        export_type::<DomMutationType>(),
        export_type::<DomQueryType>(),
        export_type::<MatchArm>(),
        export_type::<Pattern>(),
        export_type::<ImportStatement>(),
        export_type::<ExternalResourceType>(),
        // AI types
        export_type::<AIExpression>(),
        export_type::<AIStatement>(),
        export_type::<AIMetadata>(),
        export_type::<ToolCall>(),
        export_type::<ExecutionStrategy>(),
        export_type::<ErrorHandling>(),
        export_type::<BackoffStrategy>(),
        export_type::<DelegationStrategy>(),
        export_type::<LearningTarget>(),
        export_type::<LearningStrategy>(),
        export_type::<AdaptationAction>(),
        export_type::<Priority>(),
        export_type::<KnowledgeType>(),
        export_type::<RetentionPolicy>(),
        export_type::<DiscoveryStrategy>(),
        export_type::<AdaptationType>(),
        export_type::<ParameterSchema>(),
        export_type::<ToolSchema>(),
        export_type::<LearningSource>(),
        export_type::<AdaptationStrategy>(),
        export_type::<ExecutionContext>(),
        export_type::<CollaborationPattern>(),
    ];

    let mut output = String::new();
    output.push_str("// Generated by ts-rs from crush-cast Rust types.\n");
    output.push_str("// Do not edit manually — re-run `cargo run -p crush-cast --bin export-ts --features ts-export`.\n\n");

    for decl in declarations {
        if decl.is_empty() {
            continue;
        }
        output.push_str(&decl);
        output.push('\n');
        output.push('\n');
    }

    let crate_root = std::path::PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let out_path = crate_root.join("bindings").join("cast.d.ts");
    std::fs::create_dir_all(out_path.parent().unwrap()).unwrap();
    std::fs::write(&out_path, output).unwrap();

    println!("Exported TypeScript bindings to {}", out_path.display());
}
